"""This script is the evaluation script for the Tier-2 pipeline paper being submitted to ICDSA-2026.
It computes the FID, KID, and LPIPS metrics for the synthetic datasets generated by our DoRA-augmented
Stable Diffusion pipeline, comparing them against the original MVTec defect images. The script is designed
to be run after the batch generation process is complete, and it will output the evaluation results in a
clear format for inclusion in our paper's results section."""

import torch
import torchvision.transforms as transforms
from torchmetrics.image.fid import FrechetInceptionDistance
from torchmetrics.image.kid import KernelInceptionDistance
from torchmetrics.image.lpip import LearnedPerceptualImagePatchSimilarity
from PIL import Image
from pathlib import Path
from tqdm import tqdm
import random
import itertools


def load_tensor_for_inception(img_path):
    """FID and KID expect uint8 tensors in range [0, 255] with shape (1, C, H, W)."""
    img = Image.open(img_path).convert("RGB")
    img = img.resize((299, 299), Image.Resampling.BILINEAR)
    return transforms.PILToTensor()(img).unsqueeze(0)


def load_tensor_for_lpips(img_path):
    """LPIPS expects float tensors in range [-1, 1] with shape (1, C, H, W)."""
    img = Image.open(img_path).convert("RGB")
    img = img.resize((256, 256), Image.Resampling.BILINEAR)
    tensor = transforms.ToTensor()(img)
    return (tensor * 2.0 - 1.0).unsqueeze(0)


def evaluate_unpaired_class(real_dir, generated_dir, class_name, defect_type, device="cuda"):
    real_dir = Path(real_dir)
    generated_dir = Path(generated_dir)

    real_files = sorted(list(real_dir.glob("*.png")))
    gen_files = sorted(list(generated_dir.glob("*.png")))

    if len(real_files) == 0 or len(gen_files) == 0:
        return None, None, None

    print(f"\nEvaluating {class_name.upper()} - {defect_type.upper()}")
    print(f"  -> Real Images: {len(real_files)} | Generated Images: {len(gen_files)}")

    # 1. Initialize Metrics
    fid = FrechetInceptionDistance(feature=2048).to(device)
    # subset_size must be smaller than the number of real images (e.g., if you have 15 real, subset=10)
    subset_size = min(10, len(real_files))
    kid = KernelInceptionDistance(subset_size=subset_size).to(device)
    lpips = LearnedPerceptualImagePatchSimilarity(net_type='vgg').to(device)

    # 2. Update Real Distribution (FID/KID)
    for real_path in tqdm(real_files, desc="Processing Real Images"):
        tensor = load_tensor_for_inception(real_path).to(device)
        fid.update(tensor, real=True)
        kid.update(tensor, real=True)

    # 3. Update Generated Distribution (FID/KID)
    for gen_path in tqdm(gen_files, desc="Processing Generated Images"):
        tensor = load_tensor_for_inception(gen_path).to(device)
        fid.update(tensor, real=False)
        kid.update(tensor, real=False)

    # 4. Compute LPIPS Diversity (Intra-class LPIPS)
    # We randomly sample 100 pairs from the generated images to calculate diversity
    lpips_scores = []
    num_pairs_to_check = min(100, len(gen_files) // 2)
    random.shuffle(gen_files)

    pair_generator = zip(gen_files[::2], gen_files[1::2])  # Create pairs

    for imgA_path, imgB_path in tqdm(itertools.islice(pair_generator, num_pairs_to_check),
                                     desc="Calculating LPIPS Diversity"):
        tensorA = load_tensor_for_lpips(imgA_path).to(device)
        tensorB = load_tensor_for_lpips(imgB_path).to(device)
        with torch.no_grad():
            score = lpips(tensorA, tensorB)
            lpips_scores.append(score.item())

    # 5. Compute Final Scores
    final_fid = fid.compute().item()
    final_kid = kid.compute()[0].item()  # KID returns (mean, std), we want mean
    final_lpips = sum(lpips_scores) / len(lpips_scores) if lpips_scores else 0.0

    print(f"  -> FID Score:   {final_fid:.4f} (Lower is better)")
    print(f"  -> KID Score:   {final_kid:.4f} (Lower is better)")
    print(f"  -> LPIPS (Div): {final_lpips:.4f} (HIGHER is better - indicates generated diversity)")

    fid.reset()
    kid.reset()

    return final_fid, final_kid, final_lpips


if __name__ == "__main__":
    device = "cuda" if torch.cuda.is_available() else "cpu"

